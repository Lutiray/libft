#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*sb;
	size_t	s_len;

	if (!s)
		return (NULL);
	s_len = ft_strlen(s);
	if (s_len <= start)
		return (ft_strdup(""));
	if (len > s_len - start)
		len = s_len - start;
	sb = malloc(len + 1);
	if (sb == NULL)
		return (NULL);
	ft_strlcpy(sb, s + start, len + 1);
	return (sb);
}
